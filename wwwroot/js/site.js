const uri = 'api/tests';
let todos = [];

function getItems() {
    fetch(uri)
        .then(response => response.json())
        .then(data => _displayItems(data))
        .catch(error => console.error('Unable to get items.', error));
}

let sendTestButton = document.getElementById("save");
sendTestButton.addEventListener("click", addItem);

